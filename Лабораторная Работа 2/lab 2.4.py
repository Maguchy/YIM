# Трансформация числа в bool
num_to_bool = 42
bool_result = bool(num_to_bool)
print("Трансформация числа в bool:", bool_result)

# Трансформация числа в float или int
num = 3.14
num_to_float = float(num)
num_to_int = int(num)
print("Трансформация числа в float:", num_to_float)
print("Трансформация числа в int:", num_to_int)

# Трансформация переменной в строку
var = 123
str_result = str(var)
print("Трансформация переменной в str:", str_result)