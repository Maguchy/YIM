# Операции с переменными типа int
num_int1 = 10
num_int2 = 3

# Возведение в степень
power_result = num_int1 ** num_int2
print("Возведение в степень (int):", power_result)

# Обычное деление
division_result = num_int1 / num_int2
print("Обычное деление (int):", division_result)

# Целочисленное деление
int_division_result = num_int1 // num_int2
print("Целочисленное деление (int):", int_division_result)

# Остаток от деления
modulus_result = num_int1 % num_int2
print("Остаток от деления (int):", modulus_result)

# Операции с переменными типа float
num_float1 = 5.0
num_float2 = 2.5

# Возведение в степень
power_result_float = num_float1 ** num_float2
print("Возведение в степень (float):", power_result_float)

# Обычное деление
division_result_float = num_float1 / num_float2
print("Обычное деление (float):", division_result_float)

# Целочисленное деление над переменными типа int и float
int_float_division_result = num_int1 // num_float2
print("Целочисленное деление (int и float):", int_float_division_result)