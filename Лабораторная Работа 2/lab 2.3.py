# Обычная строка
string1 = "Hello, World!"
print(string1)

# F-строка с использованием заранее объявленной переменной
name = "Alice"
f_string = f"My name is {name}."
print(f_string)

# Сложение двух строк в одну
string2 = "How are you?"
combined_string = string1 + " " + string2
print(combined_string)